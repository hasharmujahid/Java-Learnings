package com.company;

public class Display {
    private int x,y;
    private String manufacturer;

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public String getManufacturer() {
        return manufacturer;
    }

    public Display(int x, int y, String manufacturer) {
        this.x = x;
        this.y = y;
        this.manufacturer = manufacturer;
    }



}

