package com.company;

public class Main {

    public static void main(String[] args) {
        char input='A';
        switch (input){
            case 'A':
                System.out.println("It was A");
                break;
            case 'B':
                System.out.println("It was B");
                break;
            case 'C':
                System.out.println("It was C");
                break;
            case 'D':
                System.out.println("It was D");
                break;
            default:
                System.out.println("out of range");
        }

    }
}
